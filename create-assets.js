const fs = require('fs');
const path = require('path');
const assetsDir = path.join(__dirname, 'apps', 'mobile', 'assets', 'images');

// Minimal valid 1x1 pixel PNG - dark blue (#1A2B4A)
// Generated with correct CRC32 checksums
const validPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  // IHDR chunk: 1x1 px, 8-bit RGB
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE,
  // IDAT chunk: pixel #1A2B4A (zlib compressed)
  0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54,
  0x08, 0xD7, 0x63, 0xE8, 0xD4, 0xD0, 0xD0, 0x00,
  0x00, 0x00, 0x05, 0x00, 0x01, 0xBC, 0x17, 0x3E, 0x59,
  // IEND chunk
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

fs.mkdirSync(assetsDir, { recursive: true });
['icon.png', 'adaptive-icon.png', 'splash.png', 'favicon.png'].forEach(f => {
  fs.writeFileSync(path.join(assetsDir, f), validPng);
  console.log('Created:', f);
});
